public class BoardSettings {
    private static final String DEFAULT_PORT = "COM5";
    private String port;

    public BoardSettings() {
            port = DEFAULT_PORT;
    }

    public BoardSettings(String port) {
        this.port = port;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

}
